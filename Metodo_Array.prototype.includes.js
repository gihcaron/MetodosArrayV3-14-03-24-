// Exemplo 1 

// Determinar constante de cores 
const cores = ['Amarelo', 'Azul', 'Verde', 'Rosa'];

// Exibir e determinar (se é falso ou verdadeiro) se inclue a cor "Ciano" na lista 
console.log(cores.includes("Ciano"));

