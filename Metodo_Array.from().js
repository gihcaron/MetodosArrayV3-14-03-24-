// Metodo 9 - Array.from()

//Utilidade: Transforma um objeto iterável ou semelhante a um array em um Array

//Sintaxe: Array.from()

//Exemplo 1 ----------------------------------------------

// Declare a constante desejada a ser transformada em Array
const nome = 'Giovanna';

//Exiba o nome transformado em uma Array
console.log(Array.from(nome));

//Saída Esperada: [ 'G', 'i', 'o', 'v', 'a,' 'n', 'n', 'a' ]


// Exemplo 2 ----------------------------------------------

//Declarar uma string de números

let numeros = "28042008";

//Exibir os numeros transformados em formato de Array

console.log(Array.from(numeros));

//Saída esperada: ['2', '8', '0', '4', '2', '0', '0', '8']

