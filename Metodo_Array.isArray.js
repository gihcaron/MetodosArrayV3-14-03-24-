// Método 4: Array.isArray()

// Utilidade: O Array.isArray() é utilizado para verificar se o valor desejado/mencionado é uma Array ou não. 

//Sitaxe: Array.isArray(value)

//Exemplo 1-------------------------------------

//Insira ou determine o valor desejado

let numbers = 1;

//Exiba se o Array realmente é um Array ou não 
console.log(Array.isArray(numbers));

//Resultado esperado: False

// Exemplo 2 ------------------------------------

//Insira ou determine o valor desejado
const animals = ["Gato", "Cachorro", "Rato", "Coelho",];

//Exiba se o Array realmente é um Array ou não
console.log(Array.isArray(animals));

//Resultado esperado: True